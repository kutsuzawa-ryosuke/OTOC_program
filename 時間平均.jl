    for K in K_list
        println("K=",K,"--------------------------------")
        L = length(F)-K+1
        out = open("ハミルトニアン(-有り)/XY/パウリ(Z,X)/時間平均-RU有り/時間平均(1,10)/K=$K,β=$β.txt","a")
        for i in 1:1:L
